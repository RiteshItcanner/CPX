private extension String {
    func localized(bundle: Bundle = LanguageManager.shared.bundle,
                   tableName: String = "Localizable") -> String {
        return NSLocalizedString(self,
                                 tableName: tableName,
                                 bundle: bundle,
                                 comment: "")
    }
}

@propertyWrapper
struct Localizable {
    private let key: String

    var wrappedValue: String {
        return key.localized()
    }

    init(wrappedValue: String) {
        key = wrappedValue
    }
}

final class LanguageManager {
    static let shared = LanguageManager()

    private(set) var currentLanguage: String = "en"
    var deviceLanguage: String {
        let languageCode = Locale.current.languageCode ?? "en"
        let reverseLanguageDict: [String: String] = ["vi": "vn",
                                                     "ms": "my",
                                                     "fil-PH": "ph"]
        if let langValue = reverseLanguageDict[languageCode] {
            return langValue
        } else {
            return languageCode
        }
    }

    private(set) var currentBundle = Bundle.main
    var bundle: Bundle {
        return currentBundle
    }

    private let languageDict: [String: String] = ["ar": "ar",
                                                  "en": "en"]
    func setAppLanguage(_ languageCode: String) {
        setCurrentLanguage(languageCode)
        setCurrentBundlePath(currentLanguage)
    }

    private func setCurrentLanguage(_ languageCode: String) {
        if let langValue = languageDict[languageCode] {
            currentLanguage = langValue
        } else {
            currentLanguage = languageCode
        }
    }

    private func setCurrentBundlePath(_ languageCode: String) {
        guard let bundle = Bundle.main.path(forResource: languageCode,
                                            ofType: "lproj"),
            let langBundle = Bundle(path: bundle) else {
            currentBundle = Bundle.main
            return
        }
        currentBundle = langBundle
    }

    private init() {}
}
